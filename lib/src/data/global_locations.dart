// Helper functions to get data
List<GovernmentLocation> getGovernmentsForCountry(String countryName) {
  final country = globalLocations.firstWhere(
    (country) => country.name.toLowerCase() == countryName.toLowerCase(),
    orElse: () => globalLocations.first, // Default to first country if not found
  );
  return country.governments;
}

List<CityLocation> getCitiesForGovernment(String countryName, String governmentName) {
  final governments = getGovernmentsForCountry(countryName);
  final government = governments.firstWhere(
    (gov) => gov.name.toLowerCase() == governmentName.toLowerCase(),
    orElse: () => governments.first, // Default to first government if not found
  );
  return government.cities;
}

List<String> getStreetsForCity(String countryName, String governmentName, String cityName) {
  final cities = getCitiesForGovernment(countryName, governmentName);
  final city = cities.firstWhere(
    (city) => city.name.toLowerCase() == cityName.toLowerCase(),
    orElse: () => cities.first, // Default to first city if not found
  );
  return city.streets;
}

List<String> getAllCountries() {
  return globalLocations.map((country) => country.name).toList();
} 
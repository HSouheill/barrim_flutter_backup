  /// Get all governorates
  static List<Map<String, dynamic>> getGovernorates() {
    return List<Map<String, dynamic>>.from(_locationData['governorates']);
  }

  /// Get districts for a specific governorate
  static List<Map<String, dynamic>> getDistrictsForGovernorate(String governorateName) {
    final governorates = getGovernorates();
    for (final governorate in governorates) {
      if (governorate['governorate'] == governorateName) {
        return List<Map<String, dynamic>>.from(governorate['districts']);
      }
    }
    return [];
  }

  /// Get cities for a specific district in a governorate
  static List<String> getCitiesForDistrict(String governorateName, String districtName) {
    final districts = getDistrictsForGovernorate(governorateName);
    for (final district in districts) {
      if (district['district'] == districtName) {
        return List<String>.from(district['cities']);
      }
    }
    return [];
  }

  /// Get all districts (flattened list)
  static List<String> getAllDistricts() {
    final List<String> allDistricts = [];
    final governorates = getGovernorates();
    for (final governorate in governorates) {
      final districts = List<Map<String, dynamic>>.from(governorate['districts']);
      for (final district in districts) {
        allDistricts.add(district['district']);
      }
    }
    return allDistricts;
  }

  /// Get all cities (flattened list)
  static List<String> getAllCities() {
    final List<String> allCities = [];
    final governorates = getGovernorates();
    for (final governorate in governorates) {
      final districts = List<Map<String, dynamic>>.from(governorate['districts']);
      for (final district in districts) {
        final cities = List<String>.from(district['cities']);
        allCities.addAll(cities);
      }
    }
    return allCities;
  }

  /// Search for a governorate by name (case-insensitive)
  static Map<String, dynamic>? findGovernorateByName(String name) {
    final governorates = getGovernorates();
    for (final governorate in governorates) {
      if (governorate['governorate'].toString().toLowerCase() == name.toLowerCase()) {
        return governorate;
      }
    }
    return null;
  }

  /// Search for a district by name (case-insensitive)
  static Map<String, dynamic>? findDistrictByName(String districtName) {
    final governorates = getGovernorates();
    for (final governorate in governorates) {
      final districts = List<Map<String, dynamic>>.from(governorate['districts']);
      for (final district in districts) {
        if (district['district'].toString().toLowerCase() == districtName.toLowerCase()) {
          return {
            'district': district,
            'governorate': governorate,
          };
        }
      }
    }
    return null;
  }

  /// Get the country name
  static String getCountryName() {
    return _locationData['country'];
  }

  /// Get the full location data
  static Map<String, dynamic> getFullData() {
    return Map<String, dynamic>.from(_locationData);
  }
}

/// Data models for location data
class Governorate {
  final String name;
  final String isoCode;
  final List<District> districts;

  Governorate({
    required this.name,
    required this.isoCode,
    required this.districts,
  });

  factory Governorate.fromJson(Map<String, dynamic> json) {
    return Governorate(
      name: json['governorate']?.toString() ?? '',
      isoCode: json['iso_code']?.toString() ?? '',
      districts: (json['districts'] as List<dynamic>?)
          ?.map((d) => District.fromJson(d as Map<String, dynamic>))
          .toList() ?? [],
    );
  }

  Map<String, dynamic> toJson() {
    return {
      'governorate': name,
      'iso_code': isoCode,
      'districts': districts.map((d) => d.toJson()).toList(),
    };
  }
}

class District {
  final String name;
  final List<String> cities;

  District({
    required this.name,
    required this.cities,
  });

  factory District.fromJson(Map<String, dynamic> json) {
    return District(
      name: json['district']?.toString() ?? '',
      cities: List<String>.from(json['cities'] ?? []),
    );
  }

  Map<String, dynamic> toJson() {
    return {
      'district': name,
      'cities': cities,
    };
  }
}
